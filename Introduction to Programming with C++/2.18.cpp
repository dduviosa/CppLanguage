#include <iostream>
using namespace std;

int main() {
	cout << "a \t b \t pow(a, b))" << endl;
	cout << "1 \t 2 \t " << pow(1, 2) << endl;
	cout << "2 \t 3 \t " << pow(2, 3) << endl;
	cout << "3 \t 4 \t " << pow(3, 4) << endl;
	cout << "4 \t 5 \t " << pow(4, 5) << endl;	
	cout << "5 \t 6 \t " << pow(5, 6) << endl;

	return 0;
}